
n = input('\033[1;31mDigite algo:')
print('\033[4;34mO tipo primitivo desse valor é', type(n))
print('\033[4;32Só tem espaços ?', n.isspace())
print('\033[4;35mÉ um numero?', n.isnumeric())
print('\033[4;36mÉ alfabetico?', n.isalpha())
print('\033[4;33mÉ alfanumerico?', n.isalnum())
print('\033[4;31mEstá em maiscúlas?', n.isupper())
print('\033[4;34mEstá em minúsculas?', n.islower())
print('\033[4;33mEstá capitalizada?', n.istitle())
